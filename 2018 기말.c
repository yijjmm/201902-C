/*
다음의 입출력 결과로 부터 규칙을 추론하여 적절히 별(*)을 출력하여라.
(단, 입력의 첫번째 숫자는 홀수이다.)

입력1
--------------------------------------------
5 3 4

출력1
--------------------------------------------
*****  *****  *****
 ***    ***    ***
  *      *      *
 ***    ***    ***
*****  *****  *****

*****  *****  *****
 ***    ***    ***
  *      *      *
 ***    ***    ***
*****  *****  *****

*****  *****  *****
 ***    ***    ***
  *      *      *
 ***    ***    ***
*****  *****  *****

*****  *****  *****
 ***    ***    ***
  *      *      *
 ***    ***    ***
*****  *****  *****

입력2
--------------------------------------------
7 4 2

출력2
--------------------------------------------
*******  *******  *******  *******
 *****    *****    *****    *****
  ***      ***      ***      ***
   *        *        *        *
  ***      ***      ***      ***
 *****    *****    *****    *****
*******  *******  *******  *******

*******  *******  *******  *******
 *****    *****    *****    *****
  ***      ***      ***      ***
   *        *        *        *
  ***      ***      ***      ***
 *****    *****    *****    *****
*******  *******  *******  *******


*/

#include <stdio.h>

int main(){

    int a, b, c, i, j, k, l, m, n;
    scanf("%d %d %d", &a, &b, &c);

    //a = 첫째줄 별의 개수
    //b = 가로로 모형의 개수 (빈칸 2bytes)
    //c = 세로로 모형의 개수 (한 줄 띄기)

    for (n=0; n<c; n++)
    {
        for (i=0; i<a/2+1; i++)
        {
            for (m=0; m<b; m++)
            {
                for (j=0; j<i; j++)
                {
                    printf(" ");
                }

                for (k=0; k<a-2*i; k++)
                {
                    printf("*");
                }

                for (l=0; l<i+2; l++)
                {
                    printf(" ");
                }
            }
            printf("\n");
        }

        for (i=0; i<a/2; i++)
        {
            for (m=0; m<b; m++)
            {
                for (j=0; j<a/2-i-1; j++)
                {
                    printf(" ");
                }
                for (k=0; k<3+2*i; k++)
                {
                    printf("*");
                }
                for (l=0; l<a/2-i+1; l++)
                {
                    printf(" ");
                }
            }
            printf("\n");
        }
        printf("\n");
        printf("\n");
    }
return 0;
}
